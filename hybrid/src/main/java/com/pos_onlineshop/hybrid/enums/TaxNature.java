package com.pos_onlineshop.hybrid.enums;

/**
 * TaxNature enum representing different types of taxes in the system.
 */
public enum TaxNature {
    VAT,                    // Value Added Tax
    FAST_FOOD_TAX,          // Fast food levy
    EXCISE_DUTY,            // Excise duty on goods
    ENVIRONMENTAL_LEVY,     // Environmental protection levy
    CUSTOMS_DUTY,           // Import/export customs duty
    SURTAX,                 // Additional tax on top of other taxes
    OTHER                   // Other miscellaneous taxes
}
